# pop - é usado nas pilhas LIFO - primeiro elemento a entrar é o último elemento a sair
# para remover um objeto da lista específico, basta passar o indice

linguagens = ["python", "js", "c", "java", "csharp"]

print(linguagens.pop())  # csharp
print(linguagens.pop())  # java
print(linguagens.pop())  # c
print(linguagens.pop(0))  # python

